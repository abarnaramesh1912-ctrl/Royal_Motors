function setMinDateToToday(dateInput) {
  const now = new Date();
  const y = now.getFullYear();
  const m = String(now.getMonth() + 1).padStart(2, '0');
  const d = String(now.getDate()).padStart(2, '0');
  dateInput.min = `${y}-${m}-${d}`;
  dateInput.value = '';
}

function applyLanguage(lang) {
  const root = document.documentElement;
  root.dataset.lang = lang;

  const dict = translations[lang] || translations.en;

  // Fade out/in for smooth perceived switching.
  const els = Array.from(document.querySelectorAll('[data-i18n]'));
  els.forEach((el) => el.classList.add('lang-fade-out'));

  window.setTimeout(() => {
    els.forEach((el) => {
      const key = el.getAttribute('data-i18n');
      if (!key) return;
      if (dict[key] !== undefined) el.textContent = dict[key];
    });

    // Update placeholders via data-i18n-placeholder
    const placeholders = Array.from(document.querySelectorAll('[data-i18n-placeholder]'));
    placeholders.forEach((el) => {
      const key = el.getAttribute('data-i18n-placeholder');
      if (!key) return;
      if (dict[key] !== undefined) el.setAttribute('placeholder', dict[key]);
    });

    els.forEach((el) => {
      el.classList.remove('lang-fade-out');
      el.classList.add('lang-fade-in');
    });

    window.setTimeout(() => {
      els.forEach((el) => el.classList.remove('lang-fade-in'));
    }, 220);
  }, 140);
}

function init() {
  const langEn = document.getElementById('langEn');
  const langTa = document.getElementById('langTa');
  const navToggle = document.getElementById('navToggle');
  const siteNav = document.getElementById('siteNav');

  const current = document.documentElement.lang === 'ta' ? 'ta' : 'en';
  // Initialize placeholders/select options etc
  applyLanguage(current);

  langEn?.addEventListener('click', () => {
    langEn.classList.add('is-active');
    langTa?.classList.remove('is-active');
    document.documentElement.lang = 'en';
    applyLanguage('en');
  });
  langTa?.addEventListener('click', () => {
    langTa.classList.add('is-active');
    langEn?.classList.remove('is-active');
    document.documentElement.lang = 'ta';
    applyLanguage('ta');
  });

  navToggle?.addEventListener('click', () => {
    const isOpen = siteNav?.classList.toggle('is-open');
    navToggle.setAttribute('aria-expanded', isOpen ? 'true' : 'false');
  });

  // Close mobile menu on link click
  document.querySelectorAll('#siteNav a').forEach((a) => {
    a.addEventListener('click', () => {
      if (siteNav?.classList.contains('is-open')) {
        siteNav.classList.remove('is-open');
        navToggle?.setAttribute('aria-expanded', 'false');
      }
    });
  });

  // Booking form behavior (no backend; capture only)
  const form = document.getElementById('bookingForm');
  const result = document.getElementById('bookingResult');
  const dateInput = document.getElementById('date');
  if (dateInput) setMinDateToToday(dateInput);

  form?.addEventListener('submit', (e) => {
    e.preventDefault();
    const lang = document.documentElement.lang === 'ta' ? 'ta' : 'en';
    const dict = translations[lang] || translations.en;

    // Basic client-side validation already via required fields.
    // Store a small record locally so users feel confirmation.
    const payload = {
      name: form.elements['name']?.value || '',
      phone: form.elements['phone']?.value || '',
      bikeModel: form.elements['bikeModel']?.value || '',
      serviceType: form.elements['serviceType']?.value || '',
      date: form.elements['date']?.value || '',
      createdAt: new Date().toISOString(),
    };
    try {
      const key = 'royal-motors-bookings';
      const existing = JSON.parse(localStorage.getItem(key) || '[]');
      existing.push(payload);
      localStorage.setItem(key, JSON.stringify(existing).slice(-50));
    } catch {
      // Ignore storage failures.
    }

    result.textContent = dict.bookingThanks;
    result.classList.add('show');
    form.reset();
    if (dateInput) setMinDateToToday(dateInput);

    // Auto-hide message after a bit.
    window.setTimeout(() => result.classList.remove('show'), 8000);
  });
}

document.addEventListener('DOMContentLoaded', init);

